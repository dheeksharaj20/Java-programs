
public class IncrementOperators {
	public static void main(String args[]) {

		int a = 5; // Pre Increment
		int preIncNum = ++a;
		System.out.println("The pre incremented value is :" + preIncNum);
		System.out.println("The value of a is :" + a);

		int b = 8; // Post Increment
		int postIncNum = b++;
		System.out.println("The post incremented value is :" + postIncNum);
		System.out.println("The value of b is :" + b);
	}

}
