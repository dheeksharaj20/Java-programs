
public class LogicalOperator {
	public static void main(String args[]) {

		boolean x = true, y = false;
		System.out.println("x&&x :" + (x && x));

		System.out.println("x&&y :" + (x && y));

		System.out.println("y&&x :" + (y && x));

		System.out.println("y&&y :" + (y && y));

		System.out.println("x||x :" + (x || x));

		System.out.println("x||y :" + (x || y));

		System.out.println("y||x :" + (y || x));

		System.out.println("y||y :" + (y || y));

	}

}
