
public class RelationalOperator {
	public static void main(String args[]) {

		int a = 10, b = 20;
		boolean res1 = a < b;
		System.out.println("The result of a<b is:" + res1);

		int c = 10, d = 11;
		boolean res2 = c > d;
		System.out.println("The result of c>d is:" + res2);

		int x = 5, y = 10, z = 5;
		boolean res3 = x <= z;
		System.out.println("The result of x<=z is:" + res3);

		int e = 20, f = 15;
		boolean res4 = e >= f;
		System.out.println("The result of e>=f is:" + res4);

		int p = 8, q = 8;
		boolean res5 = p == q;
		System.out.println("The result of p==q is:" + res5);

		int r = 8, s = 8;
		boolean res6 = r != s;
		System.out.println("The result of r!=s is:" + res6);

	}
}
